#ifndef TEST_TIMER_H
#define TEST_TIMER_H

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Includes
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include "../GTest.h"
#include <core/time/GTimer.h>
#include <core/GLogger.h>

namespace rev {

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Tests
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class TimerTest : public Test
{
public:

    TimerTest(): Test(){}
    ~TimerTest() {}

    /// @brief Perform unit tests for Timer class
    virtual void perform() {

        // Create and run timer
        Timer timer;
        timer.start();

        // Run timer for a second to confirm that it works
        double elapsedTime = 0;
        while (elapsedTime < 1) {
            elapsedTime = timer.getElapsed<double>();
            Logger::LogInfo(std::to_string(elapsedTime).c_str());
        }
    }
};


/////////////////////////////////////////////////////////////////////////////////////////////////////////////
// End namespaces
}


#endif