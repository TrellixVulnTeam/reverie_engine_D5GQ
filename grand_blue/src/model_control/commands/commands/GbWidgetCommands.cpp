#include "GbWidgetCommands.h"

namespace Gb {

/////////////////////////////////////////////////////////////////////////////////////////////
EditLineEditCommand::EditLineEditCommand(CoreEngine* core, QLineEdit* lineEdit, const QString& newText) :
    UndoCommand(core, nullptr),
    m_lineEdit(lineEdit),
    m_newText(newText),
    m_oldText(lineEdit->text())
{
}
/////////////////////////////////////////////////////////////////////////////////////////////
EditLineEditCommand::~EditLineEditCommand()
{
}

/////////////////////////////////////////////////////////////////////////////////////////////
void EditLineEditCommand::redo()
{
    // Sets text and emits "textChanged" signal
    m_lineEdit->setText(m_newText);
}
/////////////////////////////////////////////////////////////////////////////////////////////
void EditLineEditCommand::undo()
{
    // Sets text and emits "textChanged" signal
    m_lineEdit->setText(m_oldText);
}


/////////////////////////////////////////////////////////////////////////////////////////////
} // End namespaces