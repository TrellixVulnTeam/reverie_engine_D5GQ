GString Uuid::UniqueName(const GString& prefix)
{
    return prefix + UniqueName();
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
GString Uuid::UniqueName()
{
    return Uuid(true).createUniqueName();
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
QString Uuid::UniqueQName()
{
    return Uuid(true).createUniqueQName();
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Uuid Uuid::NullID()
{
    return Uuid(false);
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Uuid::Uuid(const QUuid & uuid):
    QUuid(uuid)
{
#ifdef DEBUG_MODE
    if (*this != uuid) {
        throw("Error, failed to copy UUID");
    }
#endif
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Uuid::Uuid(const GString & str) :
    QUuid(str)
{
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Uuid::Uuid(const QString & str):
    QUuid(str)
{
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Uuid::Uuid(bool generate):
    QUuid()
{
    if (generate) {
        *this = std::move(QUuid::createUuid());
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Uuid::~Uuid()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Uuid& Uuid::operator=(const QUuid & uuid)
{
    if (&uuid == this) return *this;

    QUuid::operator=(uuid);

#ifdef DEBUG_MODE
    if (*this != uuid) {
        throw("Error, failed to match UUID");
    }
#endif

    return *this;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
GString Uuid::asString() const
{
    return QUuid::toString(QUuid::StringFormat::WithoutBraces);
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
QString Uuid::asQString() const
{
    return QUuid::toString(QUuid::StringFormat::WithoutBraces);
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
GString Uuid::createUniqueName() const
{
    return GString(QUuid::toString(QUuid::StringFormat::WithoutBraces));
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
QString Uuid::createUniqueQName() const
{
    return QUuid::toString(QUuid::StringFormat::WithoutBraces);
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
GString Uuid::createUniqueName(const GString & prefix) const
{
    return prefix + asString().replace('-', '_');
}
