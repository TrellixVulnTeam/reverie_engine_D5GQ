#ifndef GB_PY_COMPONENT_H
#define GB_PY_COMPONENT_H

// External
#include "../GbPythonWrapper.h"

namespace Gb {
/////////////////////////////////////////////////////////////////////////////////////////////
// Forward Declarations
/////////////////////////////////////////////////////////////////////////////////////////////
class SceneObject;

/////////////////////////////////////////////////////////////////////////////////////////////
// Class definitions
/////////////////////////////////////////////////////////////////////////////////////////////

/// @brief Class to encapsulate initialization of Component pybind wrapper
class PyComponent{
public:

    /// @brief Generate a component from a scene object
    template<typename T>
    static T* Create(const std::shared_ptr<SceneObject>& so) {
        T* c = new T(so);
        c->setIsPythonGenerated(true);
        return c;
    }

    /// @brief Wrap a behavior for Python
    static void PyInitialize(py::module_& m);

};

/////////////////////////////////////////////////////////////////////////////////////////////
} // End namespaces

#endif