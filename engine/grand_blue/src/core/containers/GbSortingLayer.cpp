#include "GbSortingLayer.h"

namespace Gb {

/////////////////////////////////////////////////////////////////////////////////////////////
// Class Definitions
/////////////////////////////////////////////////////////////////////////////////////////////
SortingLayer::SortingLayer(const QJsonValue & json)
{
    loadFromJson(json);
}
/////////////////////////////////////////////////////////////////////////////////////////////
QJsonValue SortingLayer::asJson() const
{
    QJsonObject object;
    object.insert("order", m_order);
    object.insert("label", m_label);
    return object;
}
/////////////////////////////////////////////////////////////////////////////////////////////
void SortingLayer::loadFromJson(const QJsonValue & json)
{
    const QJsonObject& object = json.toObject();
    m_order = object.value("order").toInt();
    m_label = object.value("label").toString();
}

/////////////////////////////////////////////////////////////////////////////////////////////
// End namespaces
}